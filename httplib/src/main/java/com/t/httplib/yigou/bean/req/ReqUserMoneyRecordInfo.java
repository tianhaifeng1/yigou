package com.t.httplib.yigou.bean.req;

/**
 * 我的余额记录列表的请求参数对象Bean
 */
public class ReqUserMoneyRecordInfo extends ReqPageInfo {

    private String operate;

    public String getOperate() {
        return operate;
    }

    public void setOperate(String operate) {
        this.operate = operate;
    }

    @Override
    public String toString() {
        return "operate" + operate +
                "page" + page +
                "pageSize" + pageSize +
                "timestamp" + timestamp  ;
    }
}
