package com.t.httplib.yigou.bean.req;

/**
 * 骑手位置请求参数Bean
 */
public class ReqHorsemanInfo extends ReqTimeInfo {

    private String horsemanId;
    private int shopId;

    public int getShopId() {
        return shopId;
    }

    public void setShopId(int shopId) {
        this.shopId = shopId;
    }

    public String getHorsemanId() {
        return horsemanId;
    }

    public void setHorsemanId(String horsemanId) {
        this.horsemanId = horsemanId;
    }

    @Override
    public String toString() {
        return "horsemanId" + horsemanId +
                "shopId" + shopId +
                "timestamp" + timestamp;
    }
}
