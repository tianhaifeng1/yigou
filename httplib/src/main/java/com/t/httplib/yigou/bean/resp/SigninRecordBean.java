package com.t.httplib.yigou.bean.resp;

public class SigninRecordBean {
    private int id;
    private String shopIntro;
    private int signinGift;
    private int shopId;
    private long signinTime;
    private int signinContinuity;
    private int signinNumber;
    private int memberId;
    private int signinGoodsId;
    private String goodsImage;
    private String goodsName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getShopIntro() {
        return shopIntro;
    }

    public void setShopIntro(String shopIntro) {
        this.shopIntro = shopIntro;
    }

    public int getSigninGift() {
        return signinGift;
    }

    public void setSigninGift(int signinGift) {
        this.signinGift = signinGift;
    }

    public int getShopId() {
        return shopId;
    }

    public void setShopId(int shopId) {
        this.shopId = shopId;
    }

    public long getSigninTime() {
        return signinTime;
    }

    public void setSigninTime(long signinTime) {
        this.signinTime = signinTime;
    }

    public int getSigninContinuity() {
        return signinContinuity;
    }

    public void setSigninContinuity(int signinContinuity) {
        this.signinContinuity = signinContinuity;
    }

    public int getSigninNumber() {
        return signinNumber;
    }

    public void setSigninNumber(int signinNumber) {
        this.signinNumber = signinNumber;
    }

    public int getMemberId() {
        return memberId;
    }

    public void setMemberId(int memberId) {
        this.memberId = memberId;
    }

    public int getSigninGoodsId() {
        return signinGoodsId;
    }

    public void setSigninGoodsId(int signinGoodsId) {
        this.signinGoodsId = signinGoodsId;
    }

    public String getGoodsImage() {
        return goodsImage;
    }

    public void setGoodsImage(String goodsImage) {
        this.goodsImage = goodsImage;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }
}
